async def main_loop():
    banner()

    console.print("[cyan]Initializing... Loading Phase Guides.[/cyan]")
    display_phase_tables()
    console.print("[green]âœ” Ariaska RL Ready. Awaiting your command.[/green]\n")

    while True:
        try:
            command = await session.prompt_async("zer0@ARIASKA > ")

            if not command.strip():
                console.print("[yellow]âš  No input. Try again.[/yellow]")
                continue

            if command.strip().lower() in ["exit", "quit"]:
                console.print("[red]Exiting Ariaska RL. Until next battle.[/red]")
                break

            if command.strip().lower() == "train-batch":
                run_replay_training()
                continue

            if command.strip().lower() == "simulate-train":
                run_simulated_environment_training()
                continue

            # ğŸ”§ Execute user command
            result = rl_agent.execute_command(command)

            if not isinstance(result, dict):
                console.print(f"[red]âŒ Invalid return value from execute_command[/red]")
                continue

            display_output(result.get("output", "[yellow]âš  No output returned.[/yellow]"))
            display_ai_hint_table(hint=None, recommendations=result.get("recommendations", []))
            show_hint()

        except (KeyboardInterrupt, EOFError):
            console.print("\n[red]âš  User interrupt. Shutting down Ariaska RL.[/red]")
            break
        except Exception as e:
            console.print(f"[bold red]âŒ Unexpected error: {e}[/bold red]")

